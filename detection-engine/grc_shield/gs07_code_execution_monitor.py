"""
GS-07 — Code Execution Sandbox Monitor
GRC-Shield Detection Engine | ASI05: Unexpected Code Execution

Intercepts all code generation and execution events by GRC AI agents.
Performs static analysis of generated code before execution —
blocking external network calls, unauthorized file system access,
and credential exfiltration attempts.

OWASP Reference: ASI05 — Unexpected Code Execution (RCE)
ISO 42001:2023: Clause 6.1.2, 8.4, 9.1
ISO 27001:2022: A.8.4, A.8.15, A.8.16, A.8.20, A.5.10

Author: Naga Sai Srinivas Matta
Repository: github.com/NSSMatta/grc-shield
"""

import ast
import hashlib
import json
import re
from dataclasses import dataclass, field
from datetime import datetime
from enum import Enum
from typing import List, Optional, Set


class CodeVerdict(Enum):
    APPROVED = "APPROVED"
    BLOCKED_EXTERNAL_NETWORK = "BLOCKED_EXTERNAL_NETWORK"
    BLOCKED_UNAUTHORIZED_FS = "BLOCKED_UNAUTHORIZED_FS"
    BLOCKED_CREDENTIAL_ACCESS = "BLOCKED_CREDENTIAL_ACCESS"
    BLOCKED_DANGEROUS_PATTERN = "BLOCKED_DANGEROUS_PATTERN"


# Approved external endpoints — only these may be called from agent-generated code
APPROVED_ENDPOINTS: Set[str] = {
    "audit-internal.company.com",
    "grc-api.company.com",
    "compliance-store.company.com",
}

# Patterns that indicate credential or secrets access
CREDENTIAL_PATTERNS = [
    r"os\.environ\[.*(KEY|SECRET|TOKEN|PASSWORD|CREDENTIAL).*\]",
    r"boto3.*credentials",
    r"\.get_credentials\(",
    r"aws_access_key",
    r"aws_secret",
    r"open\(['\"].*\.(pem|key|pfx|p12)['\"]",
]

# Patterns indicating dangerous OS-level operations
DANGEROUS_PATTERNS = [
    r"os\.system\(",
    r"subprocess\.",
    r"eval\(",
    r"exec\(",
    r"__import__\(",
    r"shutil\.rmtree",
    r"os\.remove\(",
    r"os\.unlink\(",
]


@dataclass
class CodeAnalysisResult:
    verdict: CodeVerdict
    violations: List[str]
    external_calls_detected: List[str]
    approved_calls: List[str]
    credential_access_detected: bool
    dangerous_patterns_detected: List[str]
    code_hash: str
    reason: str
    timestamp: str


class CodeExecutionSandboxMonitor:
    """
    GS-07: Static analysis gate for agent-generated code in GRC environments.

    All code generated by the GRC agent must pass static analysis before
    execution. Blocks external network calls to non-approved endpoints,
    credential access, and dangerous system operations.
    """

    def __init__(self, approved_endpoints: Optional[Set[str]] = None):
        self.approved_endpoints = approved_endpoints or APPROVED_ENDPOINTS
        self.blocked_events: List[CodeAnalysisResult] = []
        self.approved_executions: List[str] = []

    def _extract_external_calls(self, code: str) -> tuple[List[str], List[str]]:
        """Extract all external URLs/hosts from code and classify as approved or blocked."""
        # URL pattern
        url_pattern = re.compile(
            r'https?://([a-zA-Z0-9\-\.]+)(?:/[^\s\'"]*)?'
        )
        urls_found = url_pattern.findall(code)

        approved = []
        blocked = []
        for host in urls_found:
            if any(host.endswith(ep) or host == ep for ep in self.approved_endpoints):
                approved.append(host)
            else:
                blocked.append(host)

        return approved, blocked

    def _check_credential_access(self, code: str) -> List[str]:
        """Check for credential or secrets access patterns."""
        detected = []
        for pattern in CREDENTIAL_PATTERNS:
            matches = re.findall(pattern, code, re.IGNORECASE)
            if matches:
                detected.append(pattern)
        return detected

    def _check_dangerous_patterns(self, code: str) -> List[str]:
        """Check for dangerous OS-level operation patterns."""
        detected = []
        for pattern in DANGEROUS_PATTERNS:
            if re.search(pattern, code):
                detected.append(pattern)
        return detected

    def analyze(self, generated_code: str, context: Optional[str] = None) -> CodeAnalysisResult:
        """
        Perform static analysis on agent-generated code before execution.
        Returns verdict — APPROVED means safe to execute; any BLOCKED
        verdict means execution is prevented.
        """
        timestamp = datetime.utcnow().isoformat() + "Z"
        code_hash = hashlib.sha256(generated_code.encode()).hexdigest()

        violations = []

        # Check 1: External network calls
        approved_calls, blocked_calls = self._extract_external_calls(generated_code)
        if blocked_calls:
            violations.append(
                f"External network calls to non-approved endpoints: {blocked_calls}"
            )

        # Check 2: Credential access
        credential_hits = self._check_credential_access(generated_code)
        credential_detected = bool(credential_hits)
        if credential_detected:
            violations.append(f"Credential/secrets access patterns detected: {len(credential_hits)} match(es)")

        # Check 3: Dangerous patterns
        dangerous_hits = self._check_dangerous_patterns(generated_code)
        if dangerous_hits:
            violations.append(f"Dangerous OS operation patterns detected: {dangerous_hits}")

        # Determine verdict
        if blocked_calls:
            verdict = CodeVerdict.BLOCKED_EXTERNAL_NETWORK
            reason = (
                f"Code blocked: contains network calls to non-approved endpoints {blocked_calls}. "
                f"Only these endpoints are approved: {list(self.approved_endpoints)}"
            )
        elif credential_detected:
            verdict = CodeVerdict.BLOCKED_CREDENTIAL_ACCESS
            reason = "Code blocked: contains credential or secrets access patterns."
        elif dangerous_hits:
            verdict = CodeVerdict.BLOCKED_DANGEROUS_PATTERN
            reason = f"Code blocked: contains dangerous OS operation patterns: {dangerous_hits}"
        else:
            verdict = CodeVerdict.APPROVED
            reason = "Code passed static analysis. No policy violations detected."

        result = CodeAnalysisResult(
            verdict=verdict,
            violations=violations,
            external_calls_detected=blocked_calls,
            approved_calls=approved_calls,
            credential_access_detected=credential_detected,
            dangerous_patterns_detected=dangerous_hits,
            code_hash=code_hash,
            reason=reason,
            timestamp=timestamp,
        )

        if verdict != CodeVerdict.APPROVED:
            self.blocked_events.append(result)
            print(f"\n[GS-07] ⛔ {verdict.value}")
            print(f"  Code hash: {code_hash[:16]}...")
            print(f"  Reason: {reason}")
            for v in violations:
                print(f"  Violation: {v}")
        else:
            self.approved_executions.append(code_hash)
            print(f"[GS-07] ✅ APPROVED — Code hash: {code_hash[:16]}...")

        return result


